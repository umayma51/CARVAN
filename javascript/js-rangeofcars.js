            // search filter
            function filter() {
                let query = document.getElementById('search').value.toLowerCase();
                let filtered = rangeofcars.filter(item =>
                    item.name.toLowerCase().includes(query))

                if (filtered.length === 0) {
                    rangeofcars1.innerHTML = '<h2 style="text-align:center;color:white;margin-top:88px;font-size:28px">Nothing found :(</h2>';
                    return;
                }
                rangeofcars1.innerHTML = '';
                filtered.forEach(item => {
                    rangeofcars1.innerHTML +=
                        `
        <div class="card">
          <img src="${item.image}">
          <h2>${item.name}</h2>
         
          
          
          <span class="qty">${item.category}</span>
      <button class="detail" 
onclick="showDetails(this)"
price="${item.price}"
mileage="${item.mileage}"
color="${item.color}"
category="${item.category}">
details
</button>
        </div>
      `;
                });
            }





